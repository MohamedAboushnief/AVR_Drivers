/******************************************************************************
* \Syntax          : void Buzzer_TurnON(BUZZER_ID BuzzerID)
* \Description     : turn on Buzzer                                  
*                                                                             
* \Sync\Async      : Synchronous                                               
* \Reentrancy      : Non Reentrant                                             
* \Parameters (in) : BuzzerID                                                                    
* \Return value:   : None
*                                                                  
*******************************************************************************/

void Buzzer_TurnON(BUZZER_ID BuzzerID){
	switch(BuzzerID){
		case BUZZER0:
		Dio_WRITEChannel(BUZZER0_PORT,BUZZER0_CHANNEL,STD_HIGH);
		break;
		
	}
}


/******************************************************************************
* \Syntax          : void Buzzer_TurnOFF(BUZZER_ID BuzzerID)
* \Description     : turn off Buzzer
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : BuzzerID
* \Return value:   : None
*
*******************************************************************************/

void Buzzer_TurnOFF(BUZZER_ID BuzzerID){
	switch(BuzzerID){
		case BUZZER0:
		Dio_WRITEChannel(BUZZER0_PORT,BUZZER0_CHANNEL,STD_LOW);
		break;
		
	}
}

/******************************************************************************
* \Syntax          : void Buzzer_INIT(void)
* \Description     : initialize Buzzer as output
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : void
* \Return value:   : None
*
*******************************************************************************/

void Buzzer_INIT(void){
	Dio_ConfigChannel(BUZZER0_PORT,BUZZER0_CHANNEL,OUTPUT);
}

/******************************************************************************
* \Syntax          : void Buzzer_Toggle(BUZZER_ID BuzzerID)
* \Description     : Flip the output of the BUZZER
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : BuzzerID
* \Return value:   : None
*
*******************************************************************************/

void Buzzer_Toggle(BUZZER_ID BuzzerID){
	switch(BuzzerID){
		case BUZZER0:
		Dio_TOGGLEChannel(BUZZER0_PORT,BUZZER0_CHANNEL);
		break;
	}
}

