/******************************************************************************
* \Syntax          : void WDG_ON(void)
* \Description     : This service shall configure and enable WDG timer
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : None
* \Return value:   : None
*******************************************************************************/

void WDG_ON(void){

	WDT_CONTROL_REG |= WDG_WINDOW_5V_2POINT1_SEC;
	SET_BIT(WDT_CONTROL_REG,WDE);
}

/******************************************************************************
* \Syntax          : void WDG_OFF(void)
* \Description     : This service shall Disable WDG timer
*
* \Sync\Async      : Synchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : None
* \Return value:   : None
*******************************************************************************/

void WDG_OFF(void){
	WDT_CONTROL_REG =(WDG_ENABLE)|(WDG_TURN_OFF_ENABLE);
	CLEAR_BIT(WDT_CONTROL_REG,WDE);
}

/******************************************************************************
* \Syntax          : void WDG_Refresh(void)
* \Description     : This service shall Refresh WDG timer
*
* \Sync\Async      : Asynchronous
* \Reentrancy      : Non Reentrant
* \Parameters (in) : None
* \Return value:   : None
*******************************************************************************/

void WDG_Refresh(void){
	asm("WDR");
}

