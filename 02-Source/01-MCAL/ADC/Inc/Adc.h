/**********************************************************************************************************************
 *  GLOBAL DATA TYPES AND STRUCTURES
 *********************************************************************************************************************/
typedef enum{
	ADC0,
	ADC1,
	ADC2,
	ADC3,
	ADC4,
	ADC5,
	ADC6,
	ADC7
}Adc_ChannelID;

typedef enum{
	LEFT_ADJUSTED,
	RIGHT_ADJUSTED
}Adc_Result_Adjustment;

typedef enum{
	AREF,   	// Internal Vref turned off
	AVCC,	    // With external capacitor at AREF Pin
	INTERNAL    // Internal 2.56V Voltage Reference with external capacitor at AREF pin
}Voltage_Ref_Selection;

typedef enum{
	DIV_FACTOR_2,
	DIV_FACTOR_4,
	DIV_FACTOR_8,
	DIV_FACTOR_16,
	DIV_FACTOR_32,
	DIV_FACTOR_64,
	DIV_FACTOR_128
}Adc_Prescaler_Sel_Bits;

typedef enum{
	ADC_INTERRUPT,
	ADC_POLLING
}Adc_Conv_Mode;

